void display_init(){
	ssd1306_Init();
	//@TODO: bitmap drawing works correctly only for 128x64 size, needs investigation if my fault or display library's
	ssd1306_DrawBitmap(0,0,splash,128,64,White);
	ssd1306_UpdateScreen();
	osDelay(300);
	ssd1306_SetCursor(42, 56);
	char versionString[10];
	snprintf(versionString,9,"FW: %u",SOFTWARE_VERSION);
	ssd1306_WriteString(versionString, Font_6x8, White);
	ssd1306_UpdateScreen();
}

void draw(){

	//draw status bar
	ssd1306_Fill(Black);
	ssd1306_Line(0, 11, 127, 11, White);
	char statusString [25];

	inverter_state_t state = inverter.state;
	float speed = inverter.filtered_rotor_speed;
	float U_dcbus = inverter.DCbus_voltage;
	float IGBT_temp = (uint16_t) inverter.IGBT_temp;
	float current = inverter.I_RMS;
	float field_speed = inverter.stator_field_speed/(_2_PI/inverter.control_loop_freq);

	switch(state){
	//@TODO: differentiate display appearance for different inverter states
	case operation_enabled:
		if(inverter.control_mode>=1){sprintf(statusString,"RUN %+5.0fRPM %.1fA %.0fC",speed,current,IGBT_temp);}
		else{snprintf(statusString,24,"RUN %+4.1fHz %.1fA %.0fC",field_speed,current,IGBT_temp);}
		break;
	case quickstop_active:
		snprintf(statusString,24,"QSP %+5.0fRPM %3.0fV %.0fC",speed,U_dcbus,IGBT_temp);break;
	case switched_on:
		snprintf(statusString,24,"ON  %+5.0fRPM %3.0fV %.0fC",speed,U_dcbus,IGBT_temp);break;
	case ready_to_switch_on:
		snprintf(statusString,24,"RDY %+5.0fRPM %3.0fV %.0fC",speed,U_dcbus,IGBT_temp);break;
	case switch_on_disabled:
		snprintf(statusString,24,"OFF %+5.0fRPM %3.0fV %.0fC",speed,U_dcbus,IGBT_temp);break;
	case faulted:
		//@TODO: implement error number decoding to show on display
		{
		char errorname [14];
		for(uint8_t i=0;i<inverter_error_count;i++){
				if(inverter_error_object[i].error_number==inverter.error){memcpy(&errorname,&inverter_error_object[i].error_name,14);break;}
			}
		snprintf(statusString,24,"Error %s",errorname);
		break;
		}
	default:
		snprintf(statusString,24,"N/A");
		break;
	}
	ssd1306_SetCursor(2	, 0);
	ssd1306_WriteString(statusString, Font_6x8, 1);

	//draw menu
	process_buttons_state();
	refresh_func();
	//update screen
	ssd1306_UpdateScreen();
	osDelay(SCREEN_REFRESH_RATE_MS);
}

void process_buttons_state(){
	uint8_t actual_buttons_state = HAL_GPIO_ReadPin(BTN_BACK_GPIO_Port, BTN_BACK_Pin) |
			HAL_GPIO_ReadPin(BTN_UP_GPIO_Port, BTN_UP_Pin)<<1 |
			HAL_GPIO_ReadPin(BTN_DOWN_GPIO_Port, BTN_DOWN_Pin)<<2 |
			HAL_GPIO_ReadPin(BTN_ENT_GPIO_Port, BTN_ENT_Pin)<<3;
	if(actual_buttons_state!=button_state.prev_buttons_state){
		button_state.pressed_time[esc_key]=0;button_state.pressed_time[up_key]=0;button_state.pressed_time[down_key]=0;button_state.pressed_time[enter_key]=0;
		if(!HAL_GPIO_ReadPin(BTN_BACK_GPIO_Port, BTN_BACK_Pin)){if(key_back_func)(*key_back_func)();}
		if(!HAL_GPIO_ReadPin(BTN_UP_GPIO_Port, BTN_UP_Pin)){if(key_next_func)(*key_next_func)();}
		if(!HAL_GPIO_ReadPin(BTN_DOWN_GPIO_Port, BTN_DOWN_Pin)){if(key_prev_func)(*key_prev_func)();}
		if(!HAL_GPIO_ReadPin(BTN_ENT_GPIO_Port, BTN_ENT_Pin)){if(key_ent_func)(*key_ent_func)();}
	}
	if(!HAL_GPIO_ReadPin(BTN_BACK_GPIO_Port, BTN_BACK_Pin))button_state.pressed_time[esc_key]+=SCREEN_REFRESH_RATE_MS;
	if(!HAL_GPIO_ReadPin(BTN_UP_GPIO_Port, BTN_UP_Pin))button_state.pressed_time[up_key]+=SCREEN_REFRESH_RATE_MS;
	if(!HAL_GPIO_ReadPin(BTN_DOWN_GPIO_Port, BTN_DOWN_Pin))button_state.pressed_time[down_key]+=SCREEN_REFRESH_RATE_MS;
	if(!HAL_GPIO_ReadPin(BTN_ENT_GPIO_Port, BTN_ENT_Pin))button_state.pressed_time[enter_key]+=SCREEN_REFRESH_RATE_MS;

	//@TODO: implement long press
	//if(button_state.pressed_time[esc_key]>=1500)menu_long_back();button_state.pressed_time[esc_key]=0;
	if(button_state.pressed_time[up_key]>=1000){if(key_next_func)(*key_next_func)();}
	if(button_state.pressed_time[down_key]>=1000){if(key_prev_func)(*key_prev_func)();}
	//if(button_state.pressed_time[enter_key]>=1500)menu_long_enter();button_state.pressed_time[enter_key]=0;

	button_state.prev_buttons_state = actual_buttons_state;

}

